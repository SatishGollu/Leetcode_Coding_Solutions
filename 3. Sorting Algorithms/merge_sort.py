#merge sort
def merge_sort(arr):
    arr_length = len(arr)
    if arr_length > 1:
        #mid point is where arrya is divided into subarrays
        mid = arr_length//2
        left = arr[:mid]
        right = arr[mid:]

        #sort the two halves
        merge_sort(left)
        merge_sort(right)

        #now join the elements in order
        i = j = k = 0
        while i < len(left) and j < len(right):
            if left[i] < right[j]:
                arr[k] = left[i]
                i +=1
            else:
                arr[k] = right[j]
                j +=1
            k +=1
        #when we run out of elements in left or right, pickup remaining elements
        while i < len(left):
            arr[k] = left[i]
            i += 1
            k += 1
        while j < len(right):
            arr[k] = right[j]
            j += 1
            k += 1
    return arr

array = [10,14,28,11,7,16,30,50,25,18]
